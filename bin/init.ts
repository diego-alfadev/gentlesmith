#!/usr/bin/env bun
/**
 * gentlesmith init — deterministic runtime bootstrap.
 *
 * The product happy path is `gentlesmith forge`. `init` prepares the runtime
 * state it needs: dirs, one local profile when none exists, detected targets,
 * and a cached discovery summary. It never overwrites existing local profiles.
 */

import { readFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import { join } from "node:path";
import { parse as parseYAML, stringify as stringifyYAML } from "yaml";
import {
  cloneTemplateTarget,
  ensureRuntimeState,
  listInstalledTargets,
  listLocalProfiles,
  listTargetTemplates,
  markRuntimeInitialized,
  resolveRuntimePaths,
  saveInstalledTarget,
  writeRuntimeFile,
  type RuntimePaths,
} from "./runtime";
import { discoverRuntime, summarizeDiscovery, type DiscoverySnapshot } from "./discovery";

const PATHS = resolveRuntimePaths();

export interface BootstrapResult {
  profileName: string;
  profileCreated: boolean;
  installedTargets: string[];
  snapshot: DiscoverySnapshot;
}

export async function bootstrapRuntime(paths: RuntimePaths = PATHS): Promise<BootstrapResult> {
  await ensureRuntimeState(paths);
  const snapshot = await discoverRuntime(paths);
  const { profileName, created } = await ensureDefaultProfile(paths, snapshot);
  const installedTargets = await ensureRecommendedTargets(paths, snapshot, profileName);

  await markRuntimeInitialized(paths, {
    lastDiscovery: {
      recommendations: snapshot.recommendations,
    },
  });

  return {
    profileName,
    profileCreated: created,
    installedTargets,
    snapshot,
  };
}

export async function runWizard(): Promise<void> {
  console.log("gentlesmith init — runtime bootstrap\n");
  const result = await bootstrapRuntime(PATHS);

  console.log(`Profile: ${result.profileName}${result.profileCreated ? " (created)" : " (existing)"}`);
  if (result.installedTargets.length > 0) {
    console.log("Installed targets:");
    for (const target of result.installedTargets) console.log(`  - ${target}`);
  } else {
    console.log("Installed targets: no changes");
  }

  console.log("\nDiscovery:");
  for (const line of summarizeDiscovery(result.snapshot)) console.log(`  - ${line}`);

  console.log("\nNext:");
  console.log("  gentlesmith forge   # LLM-led profile refinement");
  console.log("  gentlesmith browse  # inspect/edit/apply");
}

async function ensureDefaultProfile(
  paths: RuntimePaths,
  snapshot: DiscoverySnapshot,
): Promise<{ profileName: string; created: boolean }> {
  const locals = await listLocalProfiles(paths);
  const diego = locals.find((profile) => profile.name === "diego-local" || profile.name === "local-diego");
  if (diego) return { profileName: diego.name, created: false };
  if (locals.length > 0) return { profileName: locals[0].name, created: false };

  const profileName = "local-default";
  const profilePath = join(paths.localProfilesDir, `${profileName}.yaml`);
  if (existsSync(profilePath)) return { profileName, created: false };

  const include = unique([...await readBaseIncludes(paths, "jarvis"), ...snapshot.recommendations.fragments]);
  await writeRuntimeFile(profilePath, stringifyYAML({
    name: profileName,
    description: "Generated by gentlesmith init — deterministic bootstrap profile",
    include,
  }));
  return { profileName, created: true };
}

async function readBaseIncludes(paths: RuntimePaths, baseProfile: string): Promise<string[]> {
  const path = join(paths.builtInProfilesDir, `${baseProfile}.yaml`);
  if (!existsSync(path)) return [];
  const raw = parseYAML(await readFile(path, "utf8")) as { include?: unknown };
  return Array.isArray(raw.include) ? raw.include.filter((value): value is string => typeof value === "string") : [];
}

async function ensureRecommendedTargets(
  paths: RuntimePaths,
  snapshot: DiscoverySnapshot,
  profileName: string,
): Promise<string[]> {
  const templates = new Map((await listTargetTemplates(paths)).map((target) => [target.name, target]));
  const installed = new Map((await listInstalledTargets(paths)).map((target) => [target.name, target]));
  const added: string[] = [];

  for (const targetName of snapshot.recommendations.targets) {
    if (!templates.has(targetName) || targetName === "agents-project") continue;
    const current = installed.get(targetName);
    const templateSpec = await cloneTemplateTarget(paths, targetName);

    if (current) {
      await saveInstalledTarget(paths, targetName, {
        ...templateSpec,
        profile: current.spec.profile || profileName,
        enabled: current.spec.enabled ?? templateSpec.enabled,
      });
      continue;
    }

    await saveInstalledTarget(paths, targetName, { ...templateSpec, profile: profileName });
    added.push(targetName);
  }

  return added;
}

function unique(values: string[]): string[] {
  return Array.from(new Set(values.filter((value) => value.trim().length > 0)));
}
