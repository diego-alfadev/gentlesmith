#!/usr/bin/env bun
/**
 * gentlesmith init — interactive first-run wizard
 *
 * Creates a local profile (profiles/local-<slug>.yaml) and optionally a
 * custom env fragment (fragments-local/env/custom.md) based on 5 questions.
 * Invoked via `gentlesmith init` — dispatched from distribute.ts.
 */

import { readdir, readFile, writeFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import { join, resolve } from "node:path";
import { spawnSync } from "node:child_process";
import { input, select, confirm } from "@inquirer/prompts";
import type { ExitPromptError as ExitPromptErrorType } from "@inquirer/core";
import { parse as parseYAML } from "yaml";

const ROOT = resolve(import.meta.dir, "..");
const PROFILES_DIR = join(ROOT, "profiles");
const FRAGMENTS_LOCAL_DIR = join(ROOT, "fragments-local");

// ── Types ─────────────────────────────────────────────────────────────────────

export interface WizardAnswers {
  handle: string;           // Q1 — name/handle → profile slug
  baseProfile: string;      // Q2 — jarvis | surgical | custom
  includeEnv: boolean;      // Q3 — include env fragments?
  workingDir: string;       // Q4 — optional project/working dir context
  skills: string[];         // Q5 — optional skill packages (space-separated input)
}

// ── Questions ─────────────────────────────────────────────────────────────────

async function askQuestions(): Promise<WizardAnswers> {
  // Q1: handle/name
  const handle = await input({
    message: "Your name or handle (used in profile slug):",
    validate: (v) => v.trim().length > 0 || "Cannot be empty",
    transformer: (v) => v.trim(),
  });

  // Q2: base profile
  const availableProfiles = await listBaseProfiles();
  const profileChoices: Array<{ name: string; value: string }> = [
    ...availableProfiles.map((p) => ({ name: p, value: p })),
    { name: "custom (empty — add fragments manually)", value: "custom" },
  ];
  const baseProfile = await select({
    message: "Base profile to start from:",
    choices: profileChoices,
  });

  // Q3: include env fragments
  const includeEnv = await confirm({
    message: "Include system env fragments (env/system, env/toolchain)?",
    default: true,
  });

  // Q4: working directory context (optional)
  const workingDir = await input({
    message: "Working directory / project path for custom env context (blank to skip):",
    transformer: (v) => v.trim(),
  });

  // Q5: skill packages (optional)
  const skillsRaw = await input({
    message: "Skill packages to declare (space-separated, blank to skip):",
    transformer: (v) => v.trim(),
  });
  const skills = skillsRaw.length > 0
    ? skillsRaw.split(/\s+/).filter((s) => s.length > 0)
    : [];

  return {
    handle: handle.trim(),
    baseProfile,
    includeEnv,
    workingDir: workingDir.trim(),
    skills,
  };
}

// ── Profile YAML writer ────────────────────────────────────────────────────────

export async function writeProfileYAML(answers: WizardAnswers): Promise<string> {
  const slug = toSlug(answers.handle);
  const profilePath = await resolveProfilePath(slug);

  const include = await buildIncludeList(answers);
  const lines: string[] = [
    `name: local-${slug}`,
    `description: "Generated by gentlesmith init"`,
    `include:`,
    ...include.map((ref) => `  - ${ref}`),
  ];

  if (answers.skills.length > 0) {
    lines.push(`skills:`);
    for (const pkg of answers.skills) {
      lines.push(`  - ${pkg}`);
    }
  }

  await writeFile(profilePath, lines.join("\n") + "\n", "utf8");
  return profilePath;
}

// ── Custom env fragment writer ────────────────────────────────────────────────

export async function writeCustomFragments(answers: WizardAnswers): Promise<void> {
  if (!answers.workingDir) return;

  const dir = join(FRAGMENTS_LOCAL_DIR, "env");
  await mkdir(dir, { recursive: true });

  const fragmentPath = join(dir, "custom.md");
  const content = [
    `# Custom Environment`,
    ``,
    `Working directory: \`${answers.workingDir}\``,
    ``,
  ].join("\n");

  await writeFile(fragmentPath, content, "utf8");
}

// ── Next-step hint ────────────────────────────────────────────────────────────

export async function suggestNextStep(profilePath: string): Promise<void> {
  const rel = profilePath.replace(ROOT + "/", "");
  console.log("");
  console.log("Profile written: " + rel);

  // Offer bun link if gentlesmith isn't already a global command.
  let globalCmd = "bun run distribute";
  if (!isGentlesmithLinked()) {
    console.log("");
    let offerLink = false;
    try {
      offerLink = await confirm({
        message: "Register `gentlesmith` as a global command via `bun link`?",
        default: true,
      });
    } catch (err) {
      if (!isExitPromptError(err)) throw err;
    }
    if (offerLink) {
      const result = spawnSync("bun", ["link"], { cwd: ROOT, stdio: "inherit" });
      if (result.status === 0) {
        globalCmd = "gentlesmith";
        console.log("Registered. Update later with: gentlesmith update");
      } else {
        console.warn("bun link failed — run it manually: cd " + ROOT + " && bun link");
      }
    }
  } else {
    globalCmd = "gentlesmith";
  }

  console.log("");
  console.log("Next steps:");
  console.log("  1. Review the profile: " + rel);
  console.log("  2. Run a dry-run:       " + globalCmd);
  console.log("  3. Apply when ready:    " + globalCmd + " --apply");
}

function isGentlesmithLinked(): boolean {
  const result = spawnSync("which", ["gentlesmith"], { stdio: "pipe" });
  return result.status === 0 && (result.stdout?.toString().trim().length ?? 0) > 0;
}

// ── Orchestrator ──────────────────────────────────────────────────────────────

export async function runWizard(): Promise<void> {
  console.log("gentlesmith init — first-run wizard");
  console.log("(5 questions, under 2 minutes)\n");

  let answers: WizardAnswers;
  try {
    answers = await askQuestions();
  } catch (err) {
    // @inquirer/prompts throws ExitPromptError on Ctrl-C
    if (isExitPromptError(err)) {
      console.log("\nAborted.");
      process.exit(0);
    }
    throw err;
  }

  // Build preview of files to be written
  const profileSlug = toSlug(answers.handle);
  const profilePath = await resolveProfilePath(profileSlug);
  const filesToWrite: string[] = [profilePath.replace(ROOT + "/", "")];
  if (answers.workingDir) {
    filesToWrite.push("fragments-local/env/custom.md");
  }

  console.log("\nFiles that will be created:");
  for (const f of filesToWrite) console.log("  " + f);

  let proceed: boolean;
  try {
    proceed = await confirm({ message: "Create these files?", default: true });
  } catch (err) {
    if (isExitPromptError(err)) {
      console.log("\nAborted.");
      process.exit(0);
    }
    throw err;
  }

  if (!proceed) {
    console.log("Aborted — no files written.");
    return;
  }

  await writeProfileYAML(answers);
  await writeCustomFragments(answers);
  await suggestNextStep(profilePath);
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function toSlug(handle: string): string {
  return handle
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

async function resolveProfilePath(slug: string): Promise<string> {
  const base = join(PROFILES_DIR, `local-${slug}.yaml`);
  if (!existsSync(base)) return base;

  // Collision suffix: -2, -3, …
  for (let i = 2; i <= 99; i++) {
    const candidate = join(PROFILES_DIR, `local-${slug}-${i}.yaml`);
    if (!existsSync(candidate)) return candidate;
  }
  // Fallback (extremely unlikely)
  return base;
}

async function listBaseProfiles(): Promise<string[]> {
  try {
    const files = await readdir(PROFILES_DIR);
    return files
      .filter((f) => f.endsWith(".yaml") && !f.startsWith("local-"))
      .map((f) => f.replace(/\.yaml$/, ""));
  } catch {
    return [];
  }
}

async function buildIncludeList(answers: WizardAnswers): Promise<string[]> {
  // "custom" → start empty, only add env if requested.
  if (answers.baseProfile === "custom") {
    const include: string[] = [];
    if (answers.includeEnv) include.push("env/system", "env/toolchain");
    if (answers.workingDir) include.push("env/custom");
    return include;
  }

  // Otherwise: read the base profile's actual include list (single source of truth).
  // Then filter env/* if user opted out, and append env/custom if a working dir was given.
  let baseInclude: string[] = [];
  try {
    const raw = await readFile(join(PROFILES_DIR, `${answers.baseProfile}.yaml`), "utf8");
    const parsed = parseYAML(raw) as { include?: unknown };
    if (Array.isArray(parsed.include)) {
      baseInclude = parsed.include.filter((x): x is string => typeof x === "string");
    }
  } catch {
    // If base profile can't be read, fall through with empty list.
  }

  const filtered = answers.includeEnv
    ? baseInclude
    : baseInclude.filter((ref) => !ref.startsWith("env/"));

  const custom = answers.workingDir && !filtered.includes("env/custom") ? ["env/custom"] : [];
  return [...filtered, ...custom];
}

function isExitPromptError(err: unknown): err is ExitPromptErrorType {
  // @inquirer/prompts throws ExitPromptError with name "ExitPromptError"
  return (
    typeof err === "object" &&
    err !== null &&
    (err as { name?: string }).name === "ExitPromptError"
  );
}
